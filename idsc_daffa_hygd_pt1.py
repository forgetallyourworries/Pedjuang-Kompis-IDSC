# ARSITEKTUR TAHAP 3: QUALITY-AWARE SWIN TRANSFORMER
# ======================================================
class QualityAwareSwin(nn.Module):
    def __init__(self, model_name='swin_base_patch4_window7_224', pretrained=True, num_classes=2):
        super(QualityAwareSwin, self).__init__()

        # ---------------------------------------------------
        # 1. CABANG VISUAL (SWIN TRANSFORMER BACKBONE)
        # ---------------------------------------------------
        # num_classes=0 membuang layer klasifikasi asli bawaan ImageNet
        # Ini menyisakan kita dengan 'Visual Feature Vector' (Pooling Layer)
        self.swin = timm.create_model(model_name, pretrained=pretrained, num_classes=0)

        # Mengambil dimensi output dari Swin (Biasanya 1024 untuk swin_base)
        swin_out_features = self.swin.num_features

        # ---------------------------------------------------
        # 2. CABANG KUALITAS (DENSE LAYER / MLP)
        # ---------------------------------------------------
        # Input: 1 nilai (Skor Kualitas 0-1)
        # Output: Vektor representasi kualitas (misal: 16 dimensi)
        self.quality_mlp = nn.Sequential(
            nn.Linear(1, 16),
            nn.GELU(), # GELU seringkali lebih baik dari ReLU di arsitektur Transformer
            nn.Linear(16, 16),
            nn.GELU()
        )

        # ---------------------------------------------------
        # 3. FUSION & FULLY CONNECTED CLASSIFIER
        # ---------------------------------------------------
        # Input: 1024 (Visual) + 16 (Quality) = 1040 dimensi
        self.classifier = nn.Sequential(
            nn.Linear(swin_out_features + 16, 512),
            nn.GELU(),
            nn.Dropout(0.3), # Mencegah overfitting
            nn.Linear(512, num_classes) # Output: 2 node (GON- dan GON+)
        )

    def forward(self, image, quality_score):
        # A. Ekstraksi Fitur Spasial
        visual_features = self.swin(image) # Shape: [Batch, 1024]

        # B. Ekstraksi Fitur Kualitas
        # Pastikan skor kualitas punya dimensi [Batch, 1] bukan cuma [Batch]
        if quality_score.dim() == 1:
            quality_score = quality_score.unsqueeze(1)

        quality_features = self.quality_mlp(quality_score) # Shape: [Batch, 16]

        # C. Fusion / Concatenation
        # Menggabungkan kedua vektor secara horizontal (dim=1)
        fused_features = torch.cat((visual_features, quality_features), dim=1) # Shape: [Batch, 1040]

        # D. Prediksi Akhir
        output = self.classifier(fused_features) # Shape: [Batch, 2]

        return output

