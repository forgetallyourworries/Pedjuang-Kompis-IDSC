# 2. SETUP TRAINING LOOP & OPTIMISASI
# ======================================================
def train_model(model, train_loader, val_loader, num_epochs=5):
    # Pindahkan model ke GPU jika tersedia
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = model.to(device)
    print(f"--- Memulai Pelatihan menggunakan device: {device} ---")

    # Inisialisasi Focal Loss
    # Karena GON- (kelas 0) adalah minoritas 27%, kita beri bobot lebih tinggi (misal 0.73)
    # GON+ (kelas 1) adalah mayoritas 73%, kita beri bobot lebih rendah (misal 0.27)
    criterion = FocalLoss(alpha=[0.73, 0.27], gamma=2.0)

    # Optimizer AdamW (Varian Adam dengan Weight Decay yang lebih baik untuk Transformer)
    optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-2)

    # Loop Pelatihan per Epoch
    for epoch in range(num_epochs):
        model.train() # Mode training (mengaktifkan Dropout & Gradient Tracking)
        running_loss = 0.0
        correct_preds = 0
        total_preds = 0

        print(f"\nEpoch {epoch+1}/{num_epochs}")
        # Progress bar menggunakan tqdm
        progress_bar = tqdm(train_loader, desc="Training", leave=False)

        for batch in progress_bar:
            # Pindahkan data ke GPU
            images = batch['image'].to(device)
            qualities = batch['quality'].to(device)
            labels = batch['label'].to(device)

            # 1. Zero Grad (Bersihkan sisa gradien dari iterasi sebelumnya)
            optimizer.zero_grad()

            # 2. Forward Pass (Prediksi)
            outputs = model(images, qualities)

            # 3. Hitung Focal Loss
            loss = criterion(outputs, labels)

            # 4. Backpropagation (Menghitung arah turunan/gradien)
            loss.backward()

            # 5. Optimization Step (Update bobot model bergerak menuju titik minimum loss)
            optimizer.step()

            # --- Perhitungan Metrik Akurasi Sederhana ---
            running_loss += loss.item()
            _, predicted = torch.max(outputs, 1)
            total_preds += labels.size(0)
            correct_preds += (predicted == labels).sum().item()

            progress_bar.set_postfix(loss=loss.item(), acc=(correct_preds/total_preds)*100)

        epoch_loss = running_loss / len(train_loader)
        epoch_acc = (correct_preds / total_preds) * 100
        print(f"Train Loss: {epoch_loss:.4f} | Train Acc: {epoch_acc:.2f}%")

        # --- (Opsional di sini: Tambahkan Validation Loop) ---

