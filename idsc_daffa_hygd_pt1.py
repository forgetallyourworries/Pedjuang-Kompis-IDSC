# 1. IMPLEMENTASI FOCAL LOSS (Mengatasi Imbalance)
# ======================================================
class FocalLoss(nn.Module):
    def __init__(self, alpha=None, gamma=2.0, reduction='mean'):
        """
        Focal Loss menekan nilai loss untuk contoh yang mudah ditebak (mayoritas)
        dan memaksa model fokus pada contoh yang sulit (minoritas GON-).
        """
        super(FocalLoss, self).__init__()
        self.gamma = gamma
        self.reduction = reduction
        # Alpha digunakan untuk memberikan bobot ekstra pada kelas minoritas
        if alpha is not None:
            self.alpha = torch.tensor(alpha, dtype=torch.float32)
        else:
            self.alpha = None

    def forward(self, inputs, targets):
        # Hitung standard Cross Entropy Loss
        ce_loss = F.cross_entropy(inputs, targets, reduction='none')

        # pt adalah probabilitas prediksi dari kelas yang benar
        pt = torch.exp(-ce_loss)

        # Terapkan faktor pemfokus (1 - pt)^gamma
        focal_loss = ((1 - pt) ** self.gamma) * ce_loss

        if self.alpha is not None:
            alpha_t = self.alpha.to(inputs.device)[targets]
            focal_loss = alpha_t * focal_loss

        if self.reduction == 'mean':
            return focal_loss.mean()
        else:
            return focal_loss.sum()

