def evaluate_metrics(model, dataloader, device):
    print("--- Memulai Evaluasi Model ---")
    model.eval() # Penting: Matikan Dropout dan BatchNorm agar hasil konsisten

    all_labels = []
    all_preds = []
    all_probs = []

    # Matikan pelacakan gradien (menghemat memori dan mempercepat proses)
    with torch.no_grad():
        for batch in dataloader:
            images = batch['image'].to(device)
            qualities = batch['quality'].to(device)
            labels = batch['label'].to(device)

            # Forward pass
            outputs = model(images, qualities)

            # Hitung probabilitas menggunakan Softmax untuk kelas 1 (GON+)
            probs = torch.softmax(outputs, dim=1)[:, 1]
            # Ambil kelas dengan nilai logit tertinggi
            _, preds = torch.max(outputs, 1)

            all_labels.extend(labels.cpu().numpy())
            all_preds.extend(preds.cpu().numpy())
            all_probs.extend(probs.cpu().numpy())

    # Hitung Metrik
    f1 = f1_score(all_labels, all_preds)
    auroc = roc_auc_score(all_labels, all_probs)
    cm = confusion_matrix(all_labels, all_preds)

    print(f"\n✅ Hasil Evaluasi:")
    print(f"F1-Score : {f1:.4f}")
    print(f"AUROC    : {auroc:.4f}")
    print("\nClassification Report:")
    print(classification_report(all_labels, all_preds, target_names=['GON-', 'GON+']))

    # Visualisasi Confusion Matrix
    plt.figure(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                xticklabels=['GON-', 'GON+'], yticklabels=['GON-', 'GON+'])
    plt.title('Confusion Matrix')
    plt.ylabel('Label Sebenarnya')
    plt.xlabel('Prediksi Model')
    plt.show()

# Cara menjalankan:
