class HYGDDataset(Dataset):
    def __init__(self, dataframe, root_dir, col_dict, transform=None):
        self.df = dataframe.reset_index(drop=True)
        self.root_dir = root_dir
        self.cols = col_dict
        self.transform = transform

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        # 1. Load Nama Gambar & Fisik Gambar
        img_name = str(self.df.loc[idx, self.cols['img']])
        if not img_name.lower().endswith(('.png', '.jpg', '.jpeg')):
            img_name += '.jpg'

        img_path = os.path.join(self.root_dir, img_name)
        image = Image.open(img_path).convert('RGB')

        # 2. Konversi Label Teks ('GON+' / 'GON-') ke Angka (1 / 0)
        raw_label = str(self.df.loc[idx, self.cols['lab']]).strip().upper()
        if raw_label == 'GON+':
            label = 1
        elif raw_label == 'GON-':
            label = 0
        else:
            # Jaga-jaga jika ternyata isinya sudah berbentuk angka 1 atau 0 dari CSV-nya
            try:
                label = int(float(raw_label))
            except ValueError:
                label = 0 # Default fallback

        # 3. Normalisasi Quality Score (0 - 1)
        quality = float(self.df.loc[idx, self.cols['qty']]) / 10.0

        if self.transform:
            image = self.transform(image)

        return {
            'image': image,
            'label': torch.tensor(label, dtype=torch.long),
            'quality': torch.tensor(quality, dtype=torch.float32)
        }

