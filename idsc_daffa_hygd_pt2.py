# 5. TAHAP 4: TRAINING DENGAN FOCAL LOSS
# ======================================================
class FocalLoss(nn.Module):
    def __init__(self, alpha=[0.85, 0.15], gamma=2.0): # Bobot ekstrem untuk imbalanced
        super(FocalLoss, self).__init__()
        self.alpha = torch.tensor(alpha, dtype=torch.float32).to(device)
        self.gamma = gamma

    def forward(self, inputs, targets):
        ce_loss = F.cross_entropy(inputs, targets, reduction='none')
        pt = torch.exp(-ce_loss)
        focal_loss = self.alpha[targets] * ((1 - pt) ** self.gamma) * ce_loss
        return focal_loss.mean()

# Parameter Training
epochs = 5 # Set ke 15-20 jika punya waktu luang
criterion = FocalLoss()
optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-2)
scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.5, patience=2)

print("\n--- Memulai Training ---")
for epoch in range(epochs):
    model.train()
    running_loss, correct, total = 0.0, 0, 0

    pbar = tqdm(train_loader, desc=f"Epoch {epoch+1}/{epochs}")
    for batch in pbar:
        img, qty, lbl = batch['image'].to(device), batch['quality'].to(device), batch['label'].to(device)

        optimizer.zero_grad()
        out = model(img, qty)
        loss = criterion(out, lbl)
        loss.backward()
        optimizer.step()

        running_loss += loss.item()
        _, pred = torch.max(out, 1)
        total += lbl.size(0)
        correct += (pred == lbl).sum().item()
        pbar.set_postfix(loss=loss.item())

    train_acc = correct/total * 100
    print(f"Epoch {epoch+1} | Loss: {running_loss/len(train_loader):.4f} | Acc: {train_acc:.2f}%")
    scheduler.step(running_loss)

