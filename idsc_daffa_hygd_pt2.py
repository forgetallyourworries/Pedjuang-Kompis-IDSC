def show_heatmap(idx):
    sample = val_ds[idx]
    img, qty, lbl = sample['image'].unsqueeze(0).to(device), sample['quality'].unsqueeze(0).to(device), sample['label'].item()

    grad_cam = MathGradCAM(model, model.swin.layers[-1].blocks[-1])
    cam_raw = grad_cam.generate(img, qty, lbl)

    cam_rsz = cv2.resize(cam_raw, (224, 224), interpolation=cv2.INTER_CUBIC)

    img_disp = np.clip(np.array([0.229, 0.224, 0.225]) * img.cpu().squeeze().permute(1, 2, 0).numpy() + np.array([0.485, 0.456, 0.406]), 0, 1)

    hm = np.float32(cv2.applyColorMap(np.uint8(255 * cam_rsz), cv2.COLORMAP_JET))[..., ::-1] / 255
    overlay = np.clip(hm * 0.4 + img_disp * 0.6, 0, 1)

    plt.figure(figsize=(10, 4))
    for i, (pic, tit) in enumerate(zip([img_disp, cam_rsz, overlay], ["Asli", "Aktivasi", "Overlay"])):
        plt.subplot(1, 3, i+1); plt.imshow(pic, cmap='jet' if i==1 else None); plt.title(tit); plt.axis('off')
    plt.suptitle(f"Kelas Asli: {'GON+' if lbl==1 else 'GON-'}", fontsize=14)
    plt.show()

